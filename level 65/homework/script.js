// კლასში დავასრულე
// 4) შექმენით Object Constructor ნებისმიერ რამეზე რომელსაც ექნება 5 property 

function Person(name, age, color, food, drink) {
    this.name = name;
    this.age = age;
    this.color = color;
    this.food = food;
    this.drink = drink;
};

// 5) შექმენით Object Constructor მოტოებზე, უნდა ჰქონდეს 4 property და ერთ-ერთი უნდა იყოს მეთოდი

function Bike(brand, model, year, horsepower) {
    this.brand = brand;
    this.model = model;
    this.year = year;
    this.horsepower = horsepower;
}
